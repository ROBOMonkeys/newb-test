def first_name(last_name):
    if last_name == " Michael":
        print ("Bolick")
    elif last_name == " Raul":
        print ("Rodriguez")
    elif last_name == " Alex":
        print ("Villegas")
    elif last_name == " Adrian":
        print ("Aldridge")
    else:
        print ("Wilson")
last_name = input("type in a first name?")
first_name(last_name)
